import streamlit as st

def display_faq():
    st.header("📚 정보교과, 궁금한 점이 있나요? (FAQ)")
    st.write("정보교과 선택을 고민하는 친구들이 자주 묻는 질문들을 모아봤어요!")

    faqs = [
        ("Q1. 코딩을 한 번도 안 해봤는데 괜찮을까요?", 
         "A. 네, 전혀 문제없어요! 정보교과는 기초부터 차근차근 배울 수 있도록 구성되어 있어요."),
        ("Q2. 수학을 잘 못하는데 정보교과를 따라갈 수 있을까요?", 
         "A. 정보교과에서 일부 수학 개념이 나오지만, 고도의 수학 실력이 필수는 아니에요."),
        ("Q3. 정보교과를 배우면 어떤 진로에 도움이 되나요?", 
         "A. 거의 모든 분야의 진로에 도움이 돼요. 특히 IT, 데이터, 인공지능 관련 분야에서 강점이 돼요."),
        ("Q4. 정보교과 수업은 딱딱하고 재미없을 것 같아요.", 
         "A. 전혀 그렇지 않아요! 코딩하고 프로젝트하면서 재미있게 배울 수 있어요.")
    ]

    for q, a in faqs:
        with st.expander(q):
            st.markdown(a, unsafe_allow_html=True)
